package com.zhongsheng.utils;


/**
 * 生成随机数的工具类
 * @version 1.0
 */
public class RandomUtils {
    
    /**
     * 生成一个[begin,end]的随机整数
     * @param begin 左边界
     * @param end   右边界
     * @return  产生的结果
     */
    public static int randomInt(int begin,int end){
        return (int)(Math.random()*(end-begin+1))+begin;
    }

}
