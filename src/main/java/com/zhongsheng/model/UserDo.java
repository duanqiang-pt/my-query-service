package com.zhongsheng.model;

import java.io.Serializable;

/**
 * @author zhongsheng
 * @date 2020年10月9日
 * @version 1.0
 */
public class UserDo implements Serializable {

	/**
	 * 序列号
	 */
	private static final long serialVersionUID = -252134563844928159L;
	
	/*
	 * 主键
	 */
	private long id;
	
	
	/*
	 * 姓名
	 */
	private String name;
	
	/*
	 * 年龄
	 */
	private int age;
	
	/*
	 * 手机号
	 */
	private String cellphone;

	public long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getCellphone() {
		return cellphone;
	}

	public void setId(long id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void setCellphone(String cellphone) {
		this.cellphone = cellphone;
	}

	@Override
	public String toString() {
		return "UserDo [id=" + id + ", name=" + name + ", age=" + age + ", cellphone=" + cellphone + "]";
	}
}
