package com.zhongsheng.component.enums;

/**
 * 排序类型枚举
 * @version 1.0
 */
public enum OrderByTypeEnum {
	
	/*
     * 升序
     */
    ASC("asc"),
    
    /*
     * 降序
     */
    DESC("desc");

	/*
	 * 排序方向
	 */
    @SuppressWarnings("unused")
	private String direction;

    OrderByTypeEnum(String direction) {
        this.direction = direction;
    }
    
}
